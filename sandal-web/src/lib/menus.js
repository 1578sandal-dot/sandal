export const canAddSide = (menu) => {
  const name = menu.name;
  if (name.endsWith("1피스") && (menu.category === "샌드위치" || menu.category === "베이글")) return true;
  if (name.endsWith("4피스") && menu.category === "유부초밥") return true;
  if (name.endsWith("반줄") && menu.category === "김밥") return true;
  return false;
};

